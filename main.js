// Addition
const add = (num1, num2) => {
  return num1 + num2;
};

// subtraction
const subtract = (num1, num2) => {
  return num1 - num2;
};

// multiplication
const multiply = (num1, num2) => {
  return num1 * num2;
};

// division
const divide = (num1, num2) => {
  return num1 / num2;
};

// CALCULATOR
const operate = (num1, operater, num2) => {
  return operater(num1, num2);
};
